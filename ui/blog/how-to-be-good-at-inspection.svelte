    <h1>How to Be Good at Inspection</h1>
    <p class="date">May 27, 2023</p>
    <p>
        In the context of speedsolving, inspection is the time you have to look at the puzzle before starting the solve. In this article, I will be going over some tips and tricks to improve your inspection.
    </p>
    <p>
        If you don't know the rules of inspection, I recommend <a class="link" href="/tutorial/inspection.html">this article</a> to brush up on the basics first.
    </p>
    <h2>Plan the entire first step</h2>
    <p>
        If you are a CFOP solver, you should plan the entire cross during inspection. Ideally you could plan even more than the cross.
    </p>
    <p>
        If you are a Roux solver, you should plan the entire first block during inspection.
    </p>
    <p>
        If your visualization skills are not quite good enough to plan the entire first step, then just try your best. <a class="link" href="/blog/practicing-with-unlimited-inspection.html">Practicing with unlimited inspection</a> is one way to improve your visualization skills.
    </p>
    <h2>Watch example solves</h2>
    <p>
        Typically the first step of any speedsolving method is the most creative step. The later steps tend to be more structured and algorithmic.
    </p>
    <p>
        A lot of cubers, including myself, struggle with being creative sometimes. Cubing tends to attract logical thinkers, but it is important to think outside of the box. Each cross solution is going to be a little bit different.
    </p>
    <p>
        To get inspiration for more creative solutions, you should look at example solves from other cubers. YouTube is a good source for example solves.
    </p>
    <h2>Use all of your inspection</h2>
    <p>
        You should make sure you are using inspection time to its fullest. This means that you should use all 15 seconds of inspection (without going over of course). If you end inspection early, you are wasting time.
    </p>
    <p>
        Oddly enough, people don't always use all of their inspection time. I have been guilty of this too. Why would we choose to throw away a valuable resource?
    </p>
    <p>
        The reason is that we get complacent, and we don't know how to effectively use the rest of our inspection time. Let's say you have finished planning the cross (or first block if you're a Roux solver). You probably think that is good enough, and there is not really enough time to plan out your first F2L pair.
    </p>
    <p>
        With that in mind, here are 3 ways you can use the rest of your inspection time. Especially if you have some time left, but not enough time to plan the entire next step.
    </p>
    <h2>Tracking pieces</h2>
    <p>
        Even if you don't have enough time to plan your first F2L pair, you can track the pieces. After you finish planning the cross, you should spot where some F2L pieces are. While solving the cross, watch those pieces so you are prepared to go straight into F2L.
    </p>
    <h2>Prediction edge orientation</h2>
    <p>
        Edge orientation is an important aspect in many methods, like F2L for CFOP, and Second Block for Roux. If you know the orientation of an edge, that cuts in half the number of cases you have to think about.
    </p>
    <p>
        For example, let's say you know that an edge will be oriented. Even this partial information is useful, because you can prepare yourself for F2L cases with oriented edges. Typically these F2L cases can be solved 2-gen (with only R and U moves).
    </p>
    <h2>Spotting problematic pieces</h2>
    <p>
        In F2L, there are some cases that are more difficult than others. For example, if pieces are in the wrong slots, or if they are hidden in the back, they can be harder to see. Pieces in the top layer or in the front are the easiest to see.
    </p>
    <p>
        If you spot these cases during inspection, that will help your lookahead. You can prepare yourself for these cases, and you will be less likely to get stuck during F2L.
    </p>
